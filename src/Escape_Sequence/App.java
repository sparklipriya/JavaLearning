package Escape_Sequence;

public class App {
    public static void main(String[] args) {
        System.out.println("Hello, \"Morning\" ");   
        System.out.println("Hello, \\Morning\\ ");   
        System.out.println("Hello, \nMorning ");
        System.out.println("Hello, \tMorning ");
        System.out.println("Hello, \bMorning\" ");
        System.out.print("*\n* *\n* * *\n* * * *\n* * * * *"); 
        System.out.println();
        System.out.print("* * * * *\n* * * *\n* * *\n* *\n*"); 
        System.out.println();
        System.out.print("        *\n      * *\n    * * *\n  * * * *\n* * * * *");
    }
    
}
